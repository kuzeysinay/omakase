"""Omakase backend.

A FastAPI service that turns a user's interests into short, social-media style
posts generated by Gemini (see `GEMINI_MODEL`), streamed back to the client via
Server-Sent Events (SSE).

Run locally:

    uvicorn main:app --reload --host 0.0.0.0 --port 8000

The iOS client connects to `POST /feed/stream` and reads SSE events as they
arrive, giving a real-time typing effect in the feed.
"""

from __future__ import annotations

import asyncio
import json
import logging
import os
import re
import uuid
from typing import AsyncIterator

import google.generativeai as genai
from dotenv import load_dotenv
from fastapi import FastAPI, HTTPException
from fastapi.middleware.cors import CORSMiddleware
from fastapi.responses import StreamingResponse
from pydantic import BaseModel, Field, field_validator

load_dotenv()

logging.basicConfig(level=logging.INFO, format="%(asctime)s %(levelname)s %(name)s: %(message)s")
logger = logging.getLogger("omakase")

GEMINI_API_KEY = os.getenv("GEMINI_API_KEY")
# Default matches current AI Studio model IDs; `gemini-1.5-flash` often 404s on v1beta now.
GEMINI_MODEL = os.getenv("GEMINI_MODEL", "gemini-2.5-flash")

if GEMINI_API_KEY:
    genai.configure(api_key=GEMINI_API_KEY)
else:
    logger.warning("GEMINI_API_KEY is not set. The /feed/stream endpoint will return an error until it is configured.")


app = FastAPI(title="Omakase", version="0.1.0")

# The iOS app talks to the backend directly, so CORS is mainly useful for
# browser-based debugging. Keep it permissive for local development.
app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],
    allow_methods=["*"],
    allow_headers=["*"],
)


class FeedRequest(BaseModel):
    """Payload sent by the iOS client to request a single generated post."""

    interests: list[str] = Field(
        default_factory=list,
        description="Free-form user interests, e.g. ['David Fincher', 'Aftersun', 'Secret Hitler'].",
    )
    seen_count: int = Field(
        default=0,
        description="Number of posts already shown; used to nudge the model toward variety.",
    )
    language: str = Field(
        default="en",
        description="BCP-47 language tag for model output (e.g. en, tr). Unknown values fall back to English.",
    )

    @field_validator("language")
    @classmethod
    def _normalize_language(cls, v: str) -> str:
        s = (v or "en").strip().lower()
        if s not in ("en", "tr"):
            return "en"
        return s


class SuggestRequest(BaseModel):
    """Payload for the interest-suggestion endpoint."""

    interests: list[str] = Field(
        default_factory=list,
        description="Interests the user has already added or is currently typing.",
    )
    draft: str = Field(
        default="",
        description="Partial text the user is typing right now (may be empty).",
    )
    exclude_suggestions: list[str] = Field(
        default_factory=list,
        description="Suggestions already shown; the model must not repeat these.",
    )
    language: str = Field(
        default="en",
        description="BCP-47 language tag for suggestion strings (en or tr).",
    )

    @field_validator("language")
    @classmethod
    def _normalize_language_suggest(cls, v: str) -> str:
        s = (v or "en").strip().lower()
        if s not in ("en", "tr"):
            return "en"
        return s


SYSTEM_PROMPT = (
    "You are the voice of Omakase — a charismatic, well-read curator who loves "
    "surprising people with things they didn't know they needed to know. "
    "Every post you write has personality: it's funny, sharp, or delightfully weird. "
    "You MUST output exactly two parts:\n"
    "1) The first line ONLY must be: TITLE: <a short, specific headline for THIS post, max 8 words> "
    "(no quotes around the title).\n"
    "2) Starting on the next line, write the post body: ONE short, self-contained piece (40-90 words) "
    "like a banger tweet from a very smart friend.\n"
    "The title must preview the body; do not use a generic label. Do not repeat 'TITLE' or the headline "
    "inside the body. Avoid hashtags, emoji, and meta commentary. "
    "Do not greet the reader. Do not mention that you are an AI."
)


def _feed_language_instruction(language: str) -> str:
    """Tells the model which natural language to use for TITLE line and body."""
    if language == "tr":
        return (
            "LANGUAGE: Write the entire post in Turkish (Türkçe). "
            "Both the TITLE: headline (after the prefix TITLE: ) and every word of the body must be Turkish. "
            "Keep the literal ASCII prefix TITLE: on the first line."
        )
    return (
        "LANGUAGE: Write the entire post in English. "
        "Both the TITLE: headline (after the prefix TITLE: ) and the body must be English."
    )


def _feed_system_instruction(language: str) -> str:
    return SYSTEM_PROMPT + "\n\n" + _feed_language_instruction(language)

